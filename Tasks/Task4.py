'''
Here we write a program to perform the basic operation of each and every operator.
'''
a = 10
b = 20

print("The + Operation is :", a + b) # The + Operation is : 30
print("The - Operation is :", a - b) # The - Operation is : -10
print("The * Operation is :", a * b) # The * Operation is : 200
print("The / Operation is :", a / b) # The / Operation is : 0.5
print("The % Operation is :", a % b) # The % Operation is : 10
print("The // Operation is :", a // b) # The // Operation is : 0
print("The ** Operation is :", a ** b) # The ** Operation is : 100000000000000000000